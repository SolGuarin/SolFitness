public class clsProducto {
    private String referencia;
    private String nombre;
    private String talla;
    private String color;
    private int precio;

    public clsProducto(String referencia, String nombre, String talla, String color, int precio) {
        this.referencia = referencia;
        this.nombre = nombre;
        this.talla = talla;
        this.color = color;
        this.precio = precio;
    }

    public clsProducto(String referenciaProducto) {
        this.referencia = referenciaProducto; //To change body of generated methods, choose Tools | Templates.
    }

    /**
     * @return the referencia
     */
    public String getReferencia() {
        return referencia;
    }

    /**
     * @param referencia the referencia to set
     */
    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }

    /**
     * @return the nombre
     */
    public String getNombre() {
        return nombre;
    }

    /**
     * @param nombre the nombre to set
     */
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    /**
     * @return the talla
     */
    public String getTalla() {
        return talla;
    }

    /**
     * @param talla the talla to set
     */
    public void setTalla(String talla) {
        this.talla = talla;
    }

    /**
     * @return the color
     */
    public String getColor() {
        return color;
    }

    /**
     * @param color the color to set
     */
    public void setColor(String color) {
        this.color = color;
    }

    /**
     * @return the precio
     */
    public int getPrecio() {
        return precio;
    }

    /**
     * @param precio the precio to set
     */
    public void setPrecio(int precio) {
        this.precio = precio;
    }


    
}
